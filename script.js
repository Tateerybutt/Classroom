function sortStudents() {
    students.sort((a, b) => parseInt(a['roll No.']) - parseInt(b['roll No.']));
}

function generateTableHeaders() {
    const headersRow = document.getElementById('tableHeaders');
    var headers = Object.keys(students[0]);
    headers.forEach(function(header) {
        var th = document.createElement('th');
        th.setAttribute('rowspan', '2');
        th.textContent = header.toUpperCase();
        headersRow.appendChild(th);
    });
}

function generateTableRows() {
    const tableBody = document.getElementById('tableBody');
    students.forEach(function(student) {
        const row = document.createElement('tr');
        Object.values(student).forEach(function(value) {
            var td = document.createElement('td');
            td.innerHTML = value;
            if (value === 'Recieved') {
                td.style.fontWeight = 'bold';
                td.innerHTML = '';
                td.style.backgroundColor = '#00ff00';
            } else if (value === 'Not Recieved') {
                td.innerHTML = '';
                td.style.color = '';
                td.style.backgroundColor = 'red';
            } else {
                td.style.color = 'black';
            }
            row.appendChild(td);
        });
        tableBody.appendChild(row);
    });
}

function createXLSX() {
    var wb = XLSX.utils.book_new();
    var ws = XLSX.utils.json_to_sheet(students);
    XLSX.utils.book_append_sheet(wb, ws, 'Students');
    XLSX.writeFile(wb, 'students.xlsx');
}


function handlePasswordInput() {
    const passwordInput = document.getElementById('password');
    const verDiv = document.getElementById('verification');
    const controls = document.getElementById('controls');
    if (passwordInput.value == '12345678') {
        verDiv.style.display = 'none';
        controls.style.display = 'block';
    } else { document.getElementById('alert').style.visibility = 'visible' };
}

